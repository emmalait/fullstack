const typeDefs = gql`
  type Book {
    title: String!
    published: Int!
    author: Author!
    genres: [String!]!
    id: ID!
  }

  type Author {
    id: ID!
    name: String!
    born: Int
    bookCount: Int
  }

  type Query {
    bookCount: Int!
    authorCount: Int!
    allBooks(author: String, genre: String): [Book!]!
    allAuthors: [Author!]!
  }

  type Mutation {
    addBook(
      title: String!
      author: String!
      published: Int!
      genres: [String]
    ): Book!
    editAuthor(name: String!, setBornTo: Int!): Author
  }
`;

const resolvers = {
  Query: {
    bookCount: async () => Book.collection.countDocuments(),
    authorCount: async () => Author.collection.countDocuments(),
    allBooks: async (root, args) => {
      let filtered = await Book.find({}).populate("author");

      if (args.author) {
        filtered = filtered.filter((book) => book.author.name === args.author);
      }

      if (args.genre) {
        filtered = filtered.filter((book) => book.genres.includes(args.genre));
      }

      return filtered;
    },
    allAuthors: async (root, args) => {
      return await Author.find({});
    },
  },
  Author: {
    bookCount: async (root) => {
      const allBooks = await Book.find({}).populate("author");
      return allBooks.filter((book) => book.author.name === root.name).length;
    },
  },
  Mutation: {
    addBook: async (root, args) => {
      let foundAuthor = await Author.findOne({ name: args.author });
      if (foundAuthor === null) {
        const new_author = new Author({ name: args.author, born: null });
        foundAuthor = await new_author.save();
      }
      const book = new Book({ ...args, author: foundAuthor });
      return await book.save();
    },
    editAuthor: async (root, args) => {
      const author = await Author.findOne({ name: args.name });
      if (author !== undefined) {
        author.born = args.setBornTo;
        return await author.save();
      } else {
        return null;
      }
    },
  },
};

const server = new ApolloServer({
  typeDefs,
  resolvers,
});

server.listen().then(({ url }) => {
  console.log(`Server ready at ${url}`);
});
